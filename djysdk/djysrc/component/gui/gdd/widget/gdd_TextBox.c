// =============================================================================
// 函数功能: 获取字符串有效字符数,TextBox只能显示单行信息，首先检查字符串,如果遇到
//           \n(换行符)，则只显示第一行字符
// 输入参数: char *str:字符串
// 输出参数: 无。
// 返回值  :第一行字符串长度
// =============================================================================
u8 __GetValidTextCount(char *str)
{
    u8 linecount=0;
    u8 count=0;
    char ch;
    u8 src_len=0;
    s32 len;
    struct FontRsc* cur_font;
    struct Charset* cur_enc;
    u32 wc;
    linecount=GetStrLineCount(str);
    if(linecount>1)
    {
        while(1)
        {
            ch=*str;
            if(ch!='\n')
            {
                src_len++;
                str++;
            }
            else
            {
                break;
            }
        }
    }
    else
    {
        src_len=strlen(str);
    }

    //计算字符串中字符数
    cur_font = Font_GetCurFont();
    cur_enc = Charset_NlsGetCurCharset();
    for(; src_len > 0;)
    {
        len= cur_enc->MbToUcs4(&wc, str, src_len);
        if(len == -1)
        { // 无效字符
            src_len--;
            str++;
        }
        else if(len == 0)
        {
            break;
        }
        else
        { // 有效字符
            str += len;
            src_len -= len;
            count++;
            continue;
        }
     }

    return count;
}

// =============================================================================
// 函数功能: 获取字符串中指定字符的地址
// 输入参数: char *str:字符串
// 输出参数: 无。
// 返回值  :第一行字符串长度
// =============================================================================
char * __GetCharAddr(char *str,u8 index)
{
    char *ch;
    struct FontRsc* cur_font;
    struct Charset* cur_enc;
    u32 wc;
    u8 src_len=0;
    s32 len;
    u8 count=0;
    src_len=strlen(str);

    if(index==1)
        return str;

    for(; src_len > 0;)
    {
        len= cur_enc->MbToUcs4(&wc, str, src_len);
        if(len == -1)
        { // 无效字符
            src_len--;
            str++;
        }
        else if(len == 0)
        {
            break;
        }
        else
        { // 有效字符
            str += len;
            src_len -= len;
            count++;
            if(count==index-1)
            {
                return str;
            }
            continue;
        }
     }
}
